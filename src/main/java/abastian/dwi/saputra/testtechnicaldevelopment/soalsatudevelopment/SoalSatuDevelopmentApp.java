package abastian.dwi.saputra.testtechnicaldevelopment.soalsatudevelopment;

public class SoalSatuDevelopmentApp {
    public static void main(String[] args) {
        /*
            Soal Test 1 Development
            LOOP ALGORITHM
            By: Abastian Dwi Saputra
         */
        int numberLength = 5;

        for (int i = 1; i <= numberLength; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print(i);
            }
            System.out.println();
        }
    }
}
